    private void btnRegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegisterActionPerformed
 String id = txtRegStudentId.getText().trim();
    String pass = txtRegPassword.getText().trim();
    String confirm = txtRegConfirm.getText().trim();

    if (id.isEmpty() || pass.isEmpty() || confirm.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Please fill in all fields.");
        return;
    }

    if (!pass.equals(confirm)) {
        JOptionPane.showMessageDialog(this, "Passwords do not match.");
        return;
    }

    try {
        boolean ok = new StudentService().register(id, pass);

        if (ok) {
            JOptionPane.showMessageDialog(this, "Registered successfully!");
            txtRegStudentId.setText("");
            txtRegPassword.setText("");
            txtRegConfirm.setText("");
        } else {
            JOptionPane.showMessageDialog(this, "Student ID already exists.");
        }
    } catch (Exception ex) {
        JOptionPane.showMessageDialog(this, "Error: " + ex.getMessage());
    }

    }//GEN-LAST:event_btnRegisterActionPerformed

    private void btnRefreshResultsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRefreshResultsActionPerformed
try {
    StudentService service = new StudentService();

        java.util.List<StudentService.ResultRow> rows =
                service.myResults(currentStudent.getStudentId());

        DefaultTableModel model = (DefaultTableModel) tblResults.getModel();
        model.setRowCount(0);

        for (StudentService.ResultRow r : rows) {
            model.addRow(new Object[]{
                r.assessmentName,
                r.type,
                r.totalMarks,
                r.weightage,
                r.marks,
                r.grade,
                r.feedbackText
            });
        }

        lblResultsStatus.setText("Status: Loaded " + rows.size() + " result(s)");

    } catch (Exception ex) {
        JOptionPane.showMessageDialog(this,
                "Error: " + ex.getMessage(),
                "Results",
                JOptionPane.ERROR_MESSAGE);
        lblResultsStatus.setText("Status: Error"); 
}
    }//GEN-LAST:event_btnRefreshResultsActionPerformed

