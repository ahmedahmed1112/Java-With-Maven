    private void btnSendCommentActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSendCommentActionPerformed

 try {
      String comment = txtComment.getText().trim();

        if (comment.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please write a comment first.");
            return;
        }

        DefaultTableModel model = (DefaultTableModel) tblComments.getModel();

        String studentName = (currentStudent != null && currentStudent.getName() != null && !currentStudent.getName().isBlank())
                ? currentStudent.getName()
                : "Me";

        String date = java.time.LocalDate.now().toString();

        model.addRow(new Object[]{ studentName, comment, date });

        txtComment.setText("");

    } catch (Exception ex) {
        JOptionPane.showMessageDialog(this, "Error: " + ex.getMessage());
       
    }//GEN-LAST:event_btnSendCommentActionPerformed
    }
    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
 try {
        String name   = txtName.getText().trim();
        String email  = txtEmail.getText().trim();
        String phone  = txtPhone.getText().trim();
        String gender = (cmbGender.getSelectedItem() == null) ? "" : cmbGender.getSelectedItem().toString().trim();
        String ageStr = txtAge.getText().trim();

        String newPassword = askNewPassword();
        if (newPassword == null) return;

        if (currentStudent == null) {
            JOptionPane.showMessageDialog(this, "No student is logged in.", "Profile", JOptionPane.ERROR_MESSAGE);
            return;
        }

        StudentService service = new StudentService();
        service.updateProfile(currentStudent, newPassword, name, gender, email, phone, ageStr);

        JOptionPane.showMessageDialog(this, "Profile saved successfully.", "Profile", JOptionPane.INFORMATION_MESSAGE);

    } catch (Exception ex) {
        JOptionPane.showMessageDialog(this, "Error: " + ex.getMessage(), "Profile", JOptionPane.ERROR_MESSAGE);
    }
}

private String askNewPassword() {
    javax.swing.JPasswordField pf = new javax.swing.JPasswordField();
    int ok = JOptionPane.showConfirmDialog(
            this,
            pf,
            "Enter new password",
            JOptionPane.OK_CANCEL_OPTION,
            JOptionPane.PLAIN_MESSAGE
    );
    if (ok != JOptionPane.OK_OPTION) return null;

    String pass = new String(pf.getPassword()).trim();
    if (pass.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Password is required.", "Profile", JOptionPane.ERROR_MESSAGE);
        return null;
    }
    return pass;
    }//GEN-LAST:event_btnSaveActionPerformed

